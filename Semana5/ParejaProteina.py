class ParejaProteina:
    def __init__(self, proteina1="", proteina2=""):
        self.proteina1 = proteina1
        self.proteina2 = proteina2
    
    def getProteina1(self):
        return self.proteina1

    def setProteina1(self, proteina1):
        self.proteina1 = proteina1

    def getProteina2(self):
        return self.proteina2

    def setProteina2(self, proteina2):
        self.proteina2 = proteina2